<script>
  import { fade, fly } from 'svelte/transition';
  import { page } from '$app/stores';
  import { Loader } from '$lib/components';

  let sideBarOpen = $state(false);
  let isDeletingNote = $state(false);

  const deleteNote = async () => {
    isDeletingNote = true;
    const id = $page.params.id;
    const res = await fetch(`/api/deleteNote`, { method: 'POST', body: JSON.stringify({ id }) });

    if (res.ok) {
      isDeletingNote = false;
      window.location.href = '/note';
    }
  }
</script>

<nav>
  <div class="flex flex-row justify-between px-4 py-2 h-14 items-center">
    {#if $page.params.id}
      <button onclick={() => {window.history.back()}} class="w-6 h-6">
        <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="h-full w-full">
          <path stroke-linecap="round" stroke-linejoin="round" d="M9 15 3 9m0 0 6-6M3 9h12a6 6 0 0 1 0 12h-3" />
        </svg>
      </button>
    {:else}
      <div class="w-6 h-6"></div>
    {/if}

    <a href='/note'>
      <h1>
        Notes
      </h1>
    </a>

    <button onclick={() => (sideBarOpen = true)}>
      <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="w-6 h-6">
        <path stroke-linecap="round" stroke-linejoin="round" d="M9.594 3.94c.09-.542.56-.94 1.11-.94h2.593c.55 0 1.02.398 1.11.94l.213 1.281c.063.374.313.686.645.87.074.04.147.083.22.127.325.196.72.257 1.075.124l1.217-.456a1.125 1.125 0 0 1 1.37.49l1.296 2.247a1.125 1.125 0 0 1-.26 1.431l-1.003.827c-.293.241-.438.613-.43.992a7.723 7.723 0 0 1 0 .255c-.008.378.137.75.43.991l1.004.827c.424.35.534.955.26 1.43l-1.298 2.247a1.125 1.125 0 0 1-1.369.491l-1.217-.456c-.355-.133-.75-.072-1.076.124a6.47 6.47 0 0 1-.22.128c-.331.183-.581.495-.644.869l-.213 1.281c-.09.543-.56.94-1.11.94h-2.594c-.55 0-1.019-.398-1.11-.94l-.213-1.281c-.062-.374-.312-.686-.644-.87a6.52 6.52 0 0 1-.22-.127c-.325-.196-.72-.257-1.076-.124l-1.217.456a1.125 1.125 0 0 1-1.369-.49l-1.297-2.247a1.125 1.125 0 0 1 .26-1.431l1.004-.827c.292-.24.437-.613.43-.991a6.932 6.932 0 0 1 0-.255c.007-.38-.138-.751-.43-.992l-1.004-.827a1.125 1.125 0 0 1-.26-1.43l1.297-2.247a1.125 1.125 0 0 1 1.37-.491l1.216.456c.356.133.751.072 1.076-.124.072-.044.146-.086.22-.128.332-.183.582-.495.644-.869l.214-1.28Z" />
        <path stroke-linecap="round" stroke-linejoin="round" d="M15 12a3 3 0 1 1-6 0 3 3 0 0 1 6 0Z" />
      </svg>
    </button>
  </div>
</nav>

{#if sideBarOpen}
  <div class="fixed inset-0 z-40 bg-black/40" transition:fade onclick={() => {sideBarOpen = false}}></div>
  <aside class="fixed top-0 bottom-0 right-0 px-4 py-2 bg-gray z-50 flex flex-col w-screen md:max-w-screen-sm" transition:fly={{ x: '100%' }}>
    <button onclick={() => (sideBarOpen = false)} class="h-12 justify-center w-wull items-end flex flex-col">
      <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="w-6 h-6">
        <path stroke-linecap="round" stroke-linejoin="round" d="M6 18 18 6M6 6l12 12" />
      </svg>
    </button>

    {#if $page.params.id}
      <button onclick={deleteNote} class="w-full px-4 py-2 hover:bg-black transition-all rounded text-start flex flex-row items-center gap-2 hover:gap-4 group">
        {#if isDeletingNote}
          <Loader />
        {:else}
          <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="w-6 h-6 transition-all group-hover:text-red">
            <path stroke-linecap="round" stroke-linejoin="round" d="m14.74 9-.346 9m-4.788 0L9.26 9m9.968-3.21c.342.052.682.107 1.022.166m-1.022-.165L18.16 19.673a2.25 2.25 0 0 1-2.244 2.077H8.084a2.25 2.25 0 0 1-2.244-2.077L4.772 5.79m14.456 0a48.108 48.108 0 0 0-3.478-.397m-12 .562c.34-.059.68-.114 1.022-.165m0 0a48.11 48.11 0 0 1 3.478-.397m7.5 0v-.916c0-1.18-.91-2.164-2.09-2.201a51.964 51.964 0 0 0-3.32 0c-1.18.037-2.09 1.022-2.09 2.201v.916m7.5 0a48.667 48.667 0 0 0-7.5 0" />
          </svg>
        {/if}
        Delete note
      </button>
    {/if}
  </aside>
{/if}
